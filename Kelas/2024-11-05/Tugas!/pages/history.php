<p class="mt-3 lead center">
    &nbsp; &nbsp; SMP Negeri 1 Gedangan, yang terletak di Jalan Rajawali 53, Kabupaten Sidoarjo, didirikan pada tahun 1982. Sekolah ini memiliki visi "Berkarakter dan Berprestasi" dan berkomitmen untuk mempersiapkan siswa-siswinya menghadapi perkembangan teknologi informasi dan komunikasi.
    Sejak berdirinya, SMP Negeri 1 Gedangan telah mengalami berbagai perkembangan dan peningkatan kualitas pendidikan. Sekolah ini menggunakan kurikulum 2013 dan memiliki berbagai fasilitas seperti laboratorium, perpustakaan, dan ruang kelas yang memadai. Selain itu, sekolah ini juga aktif dalam berbagai kegiatan ekstrakurikuler dan seni.
</p>