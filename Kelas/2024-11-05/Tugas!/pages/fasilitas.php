                <h1><u><b>Masjid</b></u></h1>
                <p>
                    Ibadah merupakan kebutuhan dasar setiap kaum muslim dan juga merupakan sarana menjalin ukhuwah Islamiyah serta sarana meningkatkan syiar Islam dan pendalaman terhadap ajaran Islam. Salah satu pusat kegiatan ibadah dan kegiatan Kajian tentang Islam adalah Masjid Al-Akbar SMP Negeri 1 Gedangan. Selain itu juga merupakan pusat kegiatan pembelajaran dan sehingga siswa bisa secara langsung melaksanakan praktik ibadah. Setiap hari senin secara bergantian siswa kelas VII, VIII, dan IX bergantian melaksanakan kegiatan salat duha berjamaah sebagai kegiatan pembiasaan penanaman karakter beriman dan bertaqwa kepada Tuhan Yang Esa. Dihari Jumat siswa juga terjadwal melaksanakan salat jumat berjamaah di masjid sekolah. Juga setiap hari siswa melaksanakan salat dhuhur secara berjamaah begitu juga dengan warga sekolah.
                    Peranan masjid sekolah pada saat ini sangat berperan untuk membina generasi yang madani, karena disamping sebagai sarana ukhuhwah Islamiyah juga sebagai sarana membantu Pendidikan non formal yang memberi dampak positif kepada warga sekolah dan masyarakat di lingkungan sekolah pada umumnya. Demi terwujudnya karakter dan akhlak mulia sesuai dengan visi sekolah, maka tidaklah terlepas dari penanaman nilai-nilai dan norma agama yang terintegrasi dalam kegiatan pembelajaran dan kegiatan keagamaan terutama dalam beribadah.
                    Masjid Al-Akbar berdiri megah dengan fasilitas tempat wudhu dalam dan luar yang terdiri dari jamaah laki-laki dan perempuan dengan kapasitas yang sama, juga dilengkapi dengan 2 kamar mandi didalam area sekitar tempat wudhu. Tampak taman asri dan hijau di halaman masjid menambah rasa syukur terhadap penciptaan alam sekitar.
