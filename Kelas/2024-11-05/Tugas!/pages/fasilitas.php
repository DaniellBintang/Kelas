                <h1><u><b>Learning Center</b></u></h1>
                <p>
                    Dalam upaya meningkatkan kualitas pendidikan di SMP Negeri 1 Gedangan, telah dibangun fasilitas outdoor yang sering disebut Learning Center. Dengan luas 5 meter x 15 meter, Learning Center menjadi ruang kelas yang berbeda dari ruang kelas konvensional.
                    Learning Center memiliki konsep yang berbeda dari kelas biasa yang terfokus pada pembelajaran indoor. Ruang kelas ini tidak hanya sebagai tempat menjejalkan pengetahuan, melainkan juga sebagai jembatan bagi siswa untuk mengembangkan empati dan kepedulian dikehidupan sehari-hari
                    Di dalam Learning Center, siswa-siswi bisa belajar dengan cara yang lebih kreatif dan menyenangkan. Mereka bisa memilih aktivitas atau materi yang ingin dipelajari sesuai dengan minat dan bakat masing-masing. Selain itu, mereka juga bisa bekerja sama dalam kelompok untuk menghasilkan ide-ide baru dan memperluas wawasan.
                    Ruang kelas yang demokratis dan menghargai keragaman akan membangun kebiasaan siswa untuk bersepakat dalam perbedaan. Ruang kelas ini akan memberikan kesempatan bagi siswa-siswi untuk mengembangkan keterampilan sosial seperti berkomunikasi, berkolaborasi, dan berkontribusi dalam mengambil keputusan bersama.
                    Learning Center juga mendukung siswa-siswi untuk memikirkan dampak tindakan yang dilakukan di masa depan. Mereka akan diberikan kesempatan untuk membuat proyek-proyek yang relevan dengan masalah-masalah sosial dan lingkungan, sehingga menjadi siswa-siswi yang peduli dan aktif dalam menjaga kelestarian alam karena learning center ini berdekatan langsung dengan hutan sekolah yang ditanami dengan tanaman ikon sekolah SMP Negeri 1 Gedangan yaitu pohon pisang
                    Dengan adanya Learning Center, diharapkan siswa-siswi tidak hanya sekedar memiliki pengetahuan, tetapi juga keterampilan dan sikap positif yang berguna dalam kehidupan sehari-hari. Learning Center menjadi salah satu pilihan dalam meningkatkan kualitas pendidikan dan membangun generasi yang Merdeka Belajar.
