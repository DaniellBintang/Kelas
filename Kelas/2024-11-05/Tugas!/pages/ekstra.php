                <p style="margin-bottom: 1rem;">
                    &nbsp &nbsp Kegiatan ekstrakurikuler Seni Tari SMP Negeri 1 Gedangan sarana yang luar biasa untuk menggali bakat seni dan kreativitas siswa. Melalui program ini, siswa tidak hanya belajar menari, tetapi juga mengembangkan keterampilan sosial, ekspresi diri, dan apresiasi terhadap keanekaragaman budaya. Dengan bimbingan dari Sugiyanto selaku pelatih ekstra tari yang berpengalaman, siswa dapat tumbuh dan berkembang dalam dunia seni tari. Menurut Kepala SMP Negeri 1 Gedangan Aris Setiawan M.Pd “bahwa program ini memberikan kesempatan pada siswa yang ingin meningkatkan kemampuan mereka dalam menari”. <br>
                    <br>
                    Tarian ini sendiri terbagi menjadi beberapa cabang, seperti tari tradisi, tari kreasi, dan tari modern. Dalam setiap cabang tari, Siswa diberikan pengetahuan dan keterampilan dalam menguasai teknik dan ekspresi yang sesuai. Siswa diajak untuk menjelajahi dan menciptakan gerakan-gerakan baru yang unik dengan tujuan mengembangkan kreativitas mereka dalam seni tari. Selain itu siswa juga dapat mengembangkan disiplin, ketekunan, dan kerjasama tim. <br>
                    <br>
                    Ekstra seni tari ini dapat membantu siswa menghargai budaya di lingkungan sekitar.,menghormati dan mengapresiasi karya seni yang diciptakan oleh orang lain terutama teman mereka sendiri sehingga menciptakan lingkungan yang positif dan inklusif.
                    Kegiatan ekstrakurikuler Seni tari di SMP Negeri 1 Gedangan yang dilaksanakan secara rutin setiap hari minggu ini tidak hanya berfokus pada aspek fisik dan teknis tari, tetapi juga pada perkembangan pribadi siswa. Dalam menari secara tidak langsung dapat membuat siswa mempelajari dan mempertunjukkan kekayaan budaya daerah mereka serta menghargai warisan budaya sendiri dan budaya lainnya, sehingga tercipta rasa saling pengertian dan toleransi sesuai dengan Profil Pelajar Pancasila
